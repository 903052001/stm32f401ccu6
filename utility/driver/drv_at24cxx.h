/*<STRUCT+>********************************************************************/
/* 结构: _AT24CXX_PARA                                                        */
/* 注释: AT24Cxx配置参数结构体                                                */
/*<STRUCT->********************************************************************/
struct _at24cxx_para
{
    unsigned int              slave_addr;            /* 从机芯片地址         */
    unsigned int              addr_width;            /* 数据地址位宽         */
    unsigned int              page_size;             /* 具体芯片page大小     */
    unsigned int              e2prom_size;           /* 具体芯片EEPROM总大小 */
    unsigned int              e2prom_holdtime;       /* 状态保持时间tLOW,/us */
    unsigned int              e2prom_wrcycle;        /* 写周期时间tWR,单位ms */
};
typedef struct _at24cxx_para *at24cxx_para_t;

/*<STRUCT+>********************************************************************/
/* 结构: AT24CXX_DEVICE                                                       */
/* 注释: AT24Cxx设备结构体                                                    */
/*<STRUCT->********************************************************************/
struct at24cxx_device
{   
    struct device             dev;                   /* AT24Cxx设备          */
    i2c_bus_dev_t             i2cx;                  /* I2Cx总线设备         */
    struct _at24cxx_para      para;                  /* AT24Cxx配置参数      */
#ifdef  BSP_USE_RT_THREAD
    rt_mutex_t                lock;                  /* AT24Cxx设备锁        */
#endif
};
typedef struct at24cxx_device *at24cxx_device_t;

