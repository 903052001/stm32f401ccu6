/*<STRUCT+>********************************************************************/
/* 结构: _SPI_BUS_CFG                                                         */
/* 注释: SPI总线配置参数                                                      */
/*<STRUCT->********************************************************************/
struct _spi_bus_cfg
{
    unsigned int         polarity;                 /* SPI极性               */
    unsigned int         phase;                    /* SPI相位               */
    unsigned int         baud;                     /* SPI波特率             */
    unsigned int         width;                    /* SPI数据位宽           */
    unsigned int         firstbit;                 /* SPI采用MSB/LSB        */
};
typedef struct _spi_bus_cfg *spi_bus_cfg_t;

/*<STRUCT+>********************************************************************/
/* 结构: _SPI_BUS_HAL                                                         */
/* 注释: SPI总线HAL库句柄                                                     */
/*<STRUCT->********************************************************************/
struct _spi_bus_hal
{
    SPI_TypeDef         *spix;                     /* SPI号                 */
    IRQn_Type            irqx;                     /* SPI中断号             */
    SPI_HandleTypeDef   *hspix;                    /* SPI设备句柄           */
};
typedef struct _spi_bus_hal *spi_bus_hal_t;

/*<STRUCT+>********************************************************************/
/* 结构: _SPI_BUS_DEV                                                         */
/* 注释: SPI总线设备                                                          */
/*<STRUCT->********************************************************************/
struct _spi_bus_dev
{
    struct device        spi_dev;                  /* SPI总线设备           */
    const  char         *spi_name;                 /* SPI总线名称           */
    spi_bus_hal_t        spi_hal;                  /* SPI总线句柄           */
    spi_bus_cfg_t        spi_cfg;                  /* SPI总线参数           */
};
typedef struct _spi_bus_dev *spi_bus_dev_t;

